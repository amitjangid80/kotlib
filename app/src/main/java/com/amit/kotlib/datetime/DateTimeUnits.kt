package com.amit.kotlib.datetime

/**
 * DateTimeUnits
 * Define units used by [DateTimeUtils.getDateDiff]
 * and also [DateTimeUtils.formatTimeStampToDate]
 *
 */
enum class DateTimeUnits {
    /**
     * Days
     */
    DAYS,

    /**
     * Hours
     */
    HOURS,

    /**
     * Minutes
     */
    MINUTES,

    /**
     * Seconds
     */
    SECONDS,

    /**
     * Milliseconds
     */
    MILLISECONDS
}
